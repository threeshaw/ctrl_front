<script setup>
import { ref, onMounted, computed } from 'vue'
import { RouterLink, RouterView } from 'vue-router'
const canvas = ref(null)
const ctx = ref(null)
const fileInput = ref(null)
const dataInChart = ref([])
// 文件数据
let files = ref([
  {
    name: '用户集',
    description: '用户分析',
    data: generateSampleData(150, 0, 100, 0, 100),
    updated: getCurrentDate(),
  },
  {
    name: '数据',
    description: '关系',
    data: generateSampleData(200, 20, 500, 5, 200),
    updated: getCurrentDate(),
  },
  {
    name: '数据集',
    description: '读数',
    data: generateSampleData(120, 10, 50, 20, 90),
    updated: getCurrentDate(),
  },
])
let leftFileList = ref(['1'])
const activeFileIndex = ref(-1)
const currentData = ref([])
const showModal = ref(false)
const modalTitle = ref('新建数据集')
const editingIndex = ref(-1)
let choice = ref(null)
const highlightedId = ref(null)

// 切换高亮状态
const toggleHighlight = (name, index) => {
  if (highlightedId.value == name) {
    // 如果点击的是已高亮的行，则取消高亮
    highlightedId.value = null
  } else {
    // 否则高亮新行
    highlightedId.value = name
  }
  console.log(highlightedId.value)
  renewChart()
  choice.value = index
}

// 获取状态文本

// 获取高亮产品的名称

const renewChart = () => {
  leftFileList.value = []
  dataInChart.value = []

  for (let i = 0; i < files.value.length; i = i + 1) {
    dataInChart.value.push([
      files.value[i].name,
      files.value[i].description,
      files.value[i].updated,
      '0',
    ])
    leftFileList.value.push(files.value[i].name)
  }
}
// 新文件数据模型
const newFile = ref({
  name: '',
  description: '',
  file: null,
  data: [],
})
// 计算数据范围
const xRange = computed(() => {
  if (!currentData.value.length) return [0, 0]
  const xs = currentData.value.map((p) => p[0])
  return [Math.min(...xs), Math.max(...xs)]
})

const yRange = computed(() => {
  if (!currentData.value.length) return [0, 0]
  const ys = currentData.value.map((p) => p[1])
  return [Math.min(...ys), Math.max(...ys)]
})

const activeFileName = computed(() => {
  return activeFileIndex.value >= 0 ? files.value[activeFileIndex.value].name : ''
})

// 打开新建文件模态框
const openNewFileModal = () => {
  resetNewFile()
  modalTitle.value = '新建数据集'
  editingIndex.value = -1
  showModal.value = true
  console.log('star')
}

// 关闭模态框
const closeModal = () => {
  showModal.value = false
}

// 重置新文件数据
const resetNewFile = () => {
  newFile.value = {
    name: '',
    description: '',
    file: null,
    data: [],
  }
}

// 触发文件选择
const triggerFileInput = () => {
  fileInput.value.click()
  console.log('startle')
}

// 处理文件上传
const handleFileUpload = (event) => {
  const file = event.target.files[0]
  if (file && file.name.endsWith('.csv')) {
    newFile.value.file = file
    parseCSV(file)
  } else {
    alert('请上传有效的CSV文件')
  }
}

// 处理文件拖放
const handleDrop = (event) => {
  event.preventDefault()
  const file = event.dataTransfer.files[0]
  if (file && file.name.endsWith('.csv')) {
    newFile.value.file = file
    parseCSV(file)
  } else {
    alert('请拖放有效的CSV文件')
  }
}

// 解析CSV文件
const parseCSV = (file) => {
  const reader = new FileReader()
  reader.onload = (e) => {
    const content = e.target.result
    const lines = content.split('\n').filter((line) => line.trim() !== '')
    const data = []

    // 跳过标题行，从第二行开始解析
    for (let i = 1; i < lines.length; i++) {
      const parts = lines[i].split(',').map((part) => part.trim())
      if (parts.length >= 2) {
        const x = parseFloat(parts[0])
        const y = parseFloat(parts[1])
        if (!isNaN(x) && !isNaN(y)) {
          data.push([x, y])
        }
      }
    }

    newFile.value.data = data
    if (!newFile.value.name) {
      newFile.value.name = file.name.replace('.csv', '')
    }
  }
  reader.readAsText(file)
}

// 添加新文件
const addNewFile = () => {
  if (!newFile.value.name) {
    alert('请输入数据集名称')
    return
  }

  if (newFile.value.data.length === 0) {
    alert('请上传有效的CSV文件')
    return
  }

  const newFileObj = {
    name: newFile.value.name,
    description: newFile.value.description || '无描述信息',
    data: newFile.value.data,
    updated: getCurrentDate(),
  }

  if (editingIndex.value >= 0) {
    // 编辑现有文件
    files.value[editingIndex.value] = newFileObj
  } else {
    // 添加新文件
    files.value.push(newFileObj)
  }

  showModal.value = false
  resetNewFile()
  renewChart()
}
//删除文件
// 加载文件数据
const loadFile = (index) => {
  activeFileIndex.value = index
  currentData.value = [...files.value[index].data]
  drawScatterPlot()
}

const delChosen = () => {
  console.log(choice.value)
  files.value.splice(choice.value, 1)
  console.log(files.value.length)
}
// 绘制散点图
const drawScatterPlot = () => {
  if (!ctx.value || !currentData.value.length) return
  const canvasEl = canvas.value
  ctx.value.clearRect(0, 0, canvasEl.width, canvasEl.height)

  // 设置画布尺寸
  const width = canvasEl.width
  const height = canvasEl.height
  const padding = 50

  // 计算比例因子
  const xMin = xRange.value[0]
  const xMax = xRange.value[1]
  const yMin = yRange.value[0]
  const yMax = yRange.value[1]

  const scaleX = (width - 2 * padding) / (xMax - xMin)
  const scaleY = (height - 2 * padding) / (yMax - yMin)

  // 绘制坐标轴
  ctx.value.strokeStyle = '#4fc3f7'
  ctx.value.lineWidth = 2
  ctx.value.beginPath()
  ctx.value.moveTo(padding, padding)
  ctx.value.lineTo(padding, height - padding)
  ctx.value.lineTo(width - padding, height - padding)
  ctx.value.stroke()

  // 绘制网格
  ctx.value.strokeStyle = 'rgba(79, 195, 247, 0.2)'
  ctx.value.lineWidth = 1

  // X轴网格
  for (let i = 1; i < 10; i++) {
    const x = padding + (i * (width - 2 * padding)) / 10
    ctx.value.beginPath()
    ctx.value.moveTo(x, padding)
    ctx.value.lineTo(x, height - padding)
    ctx.value.stroke()
  }

  // Y轴网格
  for (let i = 1; i < 10; i++) {
    const y = padding + (i * (height - 2 * padding)) / 10
    ctx.value.beginPath()
    ctx.value.moveTo(padding, y)
    ctx.value.lineTo(width - padding, y)
    ctx.value.stroke()
  }

  // 绘制折线（核心修改）
  ctx.value.beginPath()
  ctx.value.strokeStyle = '#FF5722'
  ctx.value.lineWidth = 3
  ctx.value.lineCap = 'round'
  ctx.value.lineJoin = 'round'

  currentData.value.forEach((point, index) => {
    const x = padding + (point[0] - xMin) * scaleX
    const y = height - padding - (point[1] - yMin) * scaleY

    if (index === 0) {
      ctx.value.moveTo(x, y)
    } else {
      ctx.value.lineTo(x, y)
    }
  })

  ctx.value.stroke()

  // 绘制点
  ctx.value.fillStyle = '#FF5722'
  currentData.value.forEach((point) => {
    const x = padding + (point[0] - xMin) * scaleX
    const y = height - padding - (point[1] - yMin) * scaleY

    ctx.value.beginPath()
    ctx.value.arc(x, y, 5, 0, Math.PI * 2)
    ctx.value.fill()
  })

  // 绘制标题
  ctx.value.fillStyle = '#2c3e50'
  ctx.value.font = 'bold 18px Arial'
  ctx.value.textAlign = 'center'
  ctx.value.fillText(activeFileName.value, width / 2, 30)

  // 绘制坐标轴标签
  ctx.value.font = '12px Arial'
  ctx.value.fillStyle = '#90a4ae'

  // X轴标签
  ctx.value.textAlign = 'center'
  ctx.value.fillText('X 轴', width / 2, height - 10)

  // Y轴标签
  ctx.value.save()
  ctx.value.translate(15, height / 2)
  ctx.value.rotate(-Math.PI / 2)
  ctx.value.textAlign = 'center'
  ctx.value.fillText('Y 轴', 0, 0)
  ctx.value.restore()
}

// 生成随机数据
const generateRandomData = () => {
  activeFileIndex.value = -1
  currentData.value = generateSampleData(Math.floor(Math.random() * 100) + 50, 0, 100, 0, 100)
  drawScatterPlot()
}

// 清除画布
const clearCanvas = () => {
  activeFileIndex.value = -1
  currentData.value = []
  if (ctx.value) {
    ctx.value.clearRect(0, 0, canvas.value.width, canvas.value.height)
  }
}

// 生成示例数据
function generateSampleData(count, minX, maxX, minY, maxY) {
  const data = []
  for (let i = 0; i < count; i++) {
    const x = minX + Math.random() * (maxX - minX)
    const y = minY + Math.random() * (maxY - minY)
    data.push([x, y])
  }
  return data
}

// 获取当前日期
function getCurrentDate() {
  const now = new Date()
  return `${now.getFullYear()}-${String(now.getMonth() + 1).padStart(2, '0')}-${String(now.getDate()).padStart(2, '0')}`
}

onMounted(() => {
  ctx.value = canvas.value.getContext('2d')
  renewChart()
})

defineExpose({
  triggerFileInput,
  openNewFileModal,
  renewChart,
  loadFile,
  files,
  leftFileList,
})
</script>

